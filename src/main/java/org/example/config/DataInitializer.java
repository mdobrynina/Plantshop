    private void cleanOldDefaultImages() {
        // Remove products with old default image
        productRepository.findAll().stream()
                .filter(p -> "/images/monstera.png".equals(p.getImage()) || p.getImage() == null || p.getImage().isBlank())
                .forEach(p -> { try { productRepository.delete(p); } catch (Exception ignored) {} });
        // Remove duplicates — keep only the highest-ID copy per name
        java.util.Map<String, java.util.List<org.example.model.Product>> byName = productRepository.findAll().stream()
                .collect(java.util.stream.Collectors.groupingBy(org.example.model.Product::getName));
        byName.values().stream()
                .filter(list -> list.size() > 1)
                .forEach(list -> {
                    list.sort(java.util.Comparator.comparingLong(org.example.model.Product::getId).reversed());
                    list.subList(1, list.size()).forEach(p -> { try { productRepository.delete(p); } catch (Exception ignored) {} });
                });
    }

