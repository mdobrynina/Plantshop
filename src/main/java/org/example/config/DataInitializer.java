    private Product product(String name, String imageFile,
                            String category, String categoryName,
                            String care, int price, int stock,
                            String height, String light, String watering,
                            String description) {
        return Product.builder()
                .name(name)
                .category(category)
                .categoryName(categoryName)
                .care(care)
                .price(BigDecimal.valueOf(price))
                .image("/images/products/" + imageFile.replace("img/", ""))
                .height(height)
                .light(light)
                .watering(watering)
                .description(description)
                .inStock(stock > 0)
                .stock(stock)
                .build();
    }

    private void createUserIfAbsent(String email, String rawPassword,
                                    String firstName, String lastName,
                                    LocalDate birthDate, Role role) {
        User existing = userRepository.findByEmail(email).orElse(null);
        if (existing == null) {
            userRepository.save(User.builder()
                    .email(email)
                    .password(passwordEncoder.encode(rawPassword))
                    .firstName(firstName)
                    .lastName(lastName)
                    .birthDate(birthDate)
                    .role(role)
                    .build());
        } else {
            existing.setPassword(passwordEncoder.encode(rawPassword));
            userRepository.save(existing);
        }
    }
